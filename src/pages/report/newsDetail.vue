			<h2>有关发票认证，这11个问题必须清楚！</h2>
			<div class="source">
				<span>来源：中税答疑新媒体智库、北京国税</span>
				<span>时间：2018-04-03</span>
			</div>
			<div class="content">
				<p class="boldP">
					1．小规模纳税人是否可以认证抵扣进项税额？
				</p>
				<p>
					答复：不能，只有一般纳税人才可以认证抵扣。若是小规模纳税人取得了专票，可以按照价税合计金额计入成本中。
				</p>
				<p class="boldP">
					2．发票跨年了还能不能认证？
				</p>
				<p>
					答复：可以。只要是在发票认证期限内均可以认证。
				</p>
				<p class="boldP">
					3．超过认证期的发票能否认证？
				</p>
				<p>
					答复：特殊情况下经过批准，可以认证。
				</p>
				<p>
					根据《国家税务总局关于逾期增值税扣税凭证抵扣问题的公告》（国家税务总局公告2011年第50号）和《国家税务总局关于进一步优化增值税、消费税有关涉税事项办理程序的公告》（国家税务总局公告2017年第36号）规定：
				</p>
				<p>
					 一、增值税一般纳税人发生真实交易但由于客观原因造成增值税扣税凭证（包括增值税专用发票、海关进口增值税专用缴款书和机动车销售统一发票）未能按照规定期限办理认证、确认或者稽核比对的，经主管税务机关核实、逐级上报，由省国税局认证并稽核比对后，对比对相符的增值税扣税凭证，允许纳税人继续抵扣其进项税额。
				</p>
				<p>
					增值税一般纳税人由于除本公告第二条规定以外的其他原因造成增值税扣税凭证逾期的，仍应按照增值税扣税凭证抵扣期限有关规定执行。（自2018年1月1日起施行，2018年之前逐级上报至国家税务总局进行认证并稽核比对）
				</p>
				<p>
					二、客观原因包括如下类型：
				</p>
				<p>
					（一）因自然灾害、社会突发事件等不可抗力因素造成增值税扣税凭证逾期；
				</p>
				<p>
					（二）增值税扣税凭证被盗、抢，或者因邮寄丢失、误递导致逾期；
				</p>
				<p>
					（三）有关司法、行政机关在办理业务或者检查中，扣押增值税扣税凭证，纳税人不能正常履行申报义务，或者税务机关信息系统、网络故障，未能及时处理纳税人网上认证数据等导致增值税扣税凭证逾期；
				</p>
				<p>
					（四）买卖双方因经济纠纷，未能及时传递增值税扣税凭证，或者纳税人变更纳税地点，注销旧户和重新办理税务登记的时间过长，导致增值税扣税凭证逾期；
				</p>
				<p>
					（五）由于企业办税人员伤亡、突发危重疾病或者擅自离职，未能办理交接手续，导致增值税扣税凭证逾期；
				</p>
				<p>
					（六）国家税务总局规定的其他情形。
				</p>
				<p class="boldP">
					4．取得一份公司变更名称前的专票能否认证？
				</p>
				<p>
					答复：纳税人识别号没变，发票正常没过认证期，可以认证。
				</p>
				<p class="boldP">
					5．购买方取得专票，认证时发现属于失控发票还能不能抵扣？
				</p>
				<p>
					答复：根据《国家税务总局关于失控增值税专用发票处理的批复 》（国税函〔2008〕607号）规定：“购买方主管税务机关对认证发现的失控发票，应按照规定移交稽查部门组织协查。属于销售方已申报并缴纳税款的，可由销售方主管税务机关出具书面证明，并通过协查系统回复购买方主管税务机关，该失控发票可作为购买方抵扣增值税进项税额的凭证。”
				</p>
				<p class="boldP">
					6．当月认证抵扣后开票方误操作将该发票作废如何处理？
				</p>
				<p>
					答复：根据《增值税专用发票使用规定》（国税发〔2006〕156号）第二十五条规定：“认证相符的专用发票应作为购买方的记账凭证，不得退还销售方。”
				</p>
				<p>
					销售方未按《增值税专用发票使用规定》规定的作废条件作废发票，销售方税务机关应对其进行未按规定作废发票处理，不应要求购买方退回已作废专用发票发票联和抵扣联。
				</p>
				<p class="boldP">
					7．登记为一般纳税人前的进项税额一定不能认证抵扣吗？
				</p>
				<p>
					答复：根据《关于纳税人认定或登记为一般纳税人前进项税额抵扣问题的公告》（国家税务总局公告2015年第59号）第一条规定：“纳税人自办理税务登记至认定或登记为一般纳税人期间，未取得生产经营收入，未按照销售额和征收率简易计算应纳税额申报缴纳增值税的，其在此期间取得的增值税扣税凭证，可以在认定或登记为一般纳税人后抵扣进项税额。”
				</p>
				<p class="boldP">
					8．当月发票认证后当月可以不抵扣留下月抵扣吗？
				</p>
				<p>
					答复：不可以。
				</p>
				<p>
					应在认证通过的次月申报期内，向主管税务机关申报抵扣进项税额。若是没有抵扣，相当于放弃了抵扣权利，过后应将进项税额全部计入成本费用。
				</p>
				<p class="boldP">
					9．发票认证期限360日，是360个工作日吗？
				</p>
				<p>
					答复：360日非360个工作日，节假日不能顺延，即360日为自然天数，包含节假日。是以开票日期算起，而非取得发票日期算起！
				</p>
				<p>
					根据《国家税务总局关于进一步明确营改增有关征管问题的公告》（国家税务总局公告2017年第11号）规定：自2017年7月1日起，增值税一般纳税人发票认证期限从原来180天延长至360天。
				</p>
				<p class="boldP">
					10．是不是专票只要认证了就可以抵扣增值税？
				</p>
				<p>
					答复：不一定。
				</p>
				<p>
					根据《财政部国家税务总局关于全面推开营业税改征增值税试点的通知》财税(2016)36号附件一《营业税改征增值税试点实施办法》第二十七条规定：
				</p>
				<p>
					下列项目的进项税额不得从销项税额中抵扣：
				</p>
				<p>
					（一）用于简易计税方法计税项目、免征增值税项目、集体福利或者个人消费的购进货物、加工修理修配劳务、服务、无形资产和不动产。其中涉及的固定资产、无形资产、不动产，仅指专用于上述项目的固定资产、无形资产（不包括其他权益性无形资产）、不动产。
				</p>
				<p>
					纳税人的交际应酬消费属于个人消费。
				</p>
				<p>
					（二）非正常损失的购进货物，以及相关的加工修理修配劳务和交通运输服务。
				</p>
				<p>
					（三）非正常损失的在产品、产成品所耗用的购进货物（不包括固定资产）、加工修理修配劳务和交通运输服务。
				</p>
				<p>
					（四）非正常损失的不动产，以及该不动产所耗用的购进货物、设计服务和建筑服务。
				</p>
				<p>
					（五）非正常损失的不动产在建工程所耗用的购进货物、设计服务和建筑服务。
				</p>
				<p>
					纳税人新建、改建、扩建、修缮、装饰不动产，均属于不动产在建工程。
				</p>
				<p>
					（六）购进的旅客运输服务、贷款服务、餐饮服务、居民日常服务和娱乐服务。
				</p>
				<p>
					（七）财政部和国家税务总局规定的其他情形。
				</p>
				<p class="boldP">
					11．丢失的增值税专用发票还可以认证吗？
				</p>
				<p>
					答复：根据《国家税务总局关于简化增值税发票领用和使用程序有关问题的公告》（国家税务总局公告2014年第19号 ）规定：
				</p>
				<p>
					一般纳税人丢失已开具专用发票的发票联和抵扣联，如果丢失前已认证相符的，购买方可凭销售方提供的相应专用发票记账联复印件及销售方主管税务机关出具的《丢失增值税专用发票已报税证明单》或《丢失货物运输业增值税专用发票已报税证明单》(附件1、2，以下统称《证明单》)，作为增值税进项税额的抵扣凭证；
				</p>
				<p>
					 如果丢失前未认证的，购买方凭销售方提供的相应专用发票记账联复印件进行认证，认证相符的可凭专用发票记账联复印件及销售方主管税务机关出具的《证明单》，作为增值税进项税额的抵扣凭证。专用发票记账联复印件和《证明单》留存备查。
