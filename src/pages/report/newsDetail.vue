					每年4月是纳税人申报出口退（免）税的关键期。《国家税务总局关于发布〈出口货物劳务增值税和消费税管理办法〉的公告》（国家税务总局公告2012年第24号）规定，出口企业应在货物报关出口之日次月起至次年4月30日前的各增值税纳税申报期内，收齐有关凭证（无纸化申报除外，下同）和信息，向主管税务机关申报办理出口货物增值税退（免）税及消费税退税。
