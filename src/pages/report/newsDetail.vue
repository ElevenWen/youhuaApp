			<h2>干货！一分钟了解收据、普票、增值税专票不同点</h2>
			<div class="source">
				<span>来源：中国税务报</span>
				<span>时间：2018-04-12</span>
			</div>
			<div class="content">
				<p class="boldP">
					一、收据、普票、增值税专票异同
				</p>
				<p>
					1、发票和收据
				</p>
				<p>
					发票是指一切单位和个人在购销商品、提供或接受服务以及从事其他经营活动中，所开具和收取的业务凭证，是会计核算的原始依据，也是审计机关、税务机关执法检查的重要依据。
				</p>
				<p>
					收据是指财政部门印制的盖有财政票据监制章的收付款凭证。收据一般为内部使用的临时性票据，其支付不具有法律效力，只作为证明性凭证使用。
				</p>
				<p>
					发票是国家规定的经济业务中使用的合法、有效凭证。
				</p>
				<p>
					相同点：都可以作为凭证使用，具有证明、证据性功能，并具有支付能力；
				</p>
				<p>
					不同点：发票是合法有效凭证，而收据仅作为凭证使用；发票在所有经济业务中适用，而收据只在一个单位内部使用；在税收管理上，发票是法定凭证，而收据则被认为是“白条”；发票具有税收证明功能，而收据则逃避了税收；发票种类较多，可按分为不同行业分类使用，而收据格式基本一致。
				</p>
				<br>
				<p>
					2、普通发票
				</p>
				<p>
					普通发票：普通发票是指在购销商品、提供或接受服务以及从事其他经营活动中，所开具和收取的收付款凭证。它是相对于增值税专用发票而言的。普通发票是最终用户作为买卖凭证，记账用的。
				</p>
				<br>
				<p>
					3、电子发票
				</p>
				<p>
					电子发票：电子发票是信息时代的产物，同普通发票一样，采用税务局统一发放的形式给商家使用，发票号码采用全国统一编码，采用统一防伪技术，给商家和消费者带来了更近一步的方便。
				</p>
				<p>
					对于保修的产品来说，保修的产品是电子发票，在需要使用时再打印出来，则更容易保存。不像纸质发票，偶尔会不知所踪。电子发票也是普通发票。电子发票开据后，通过发送到顾客的QQ邮箱，或者微信接收。顾客接收后通过下载A4纸打印就能使用了。发票文件一般为PDF格式，电脑需要支持此类文件才能打开和打印。
				</p>
				<br>
				<p>
					　4、增值税发票
				</p>
				<p>
					增值税专票：增值税专用发票是由国家税务总局监制设计印制的，只限于增值税一般纳税人领购使用的，既作为纳税人反映经济活动中的重要会计凭证又是兼记销货方纳税义务和购货方进项税额的合法证明；是增值税计算和管理中重要的决定性的合法的专用发票。对于增值税发票，则必须是机打的发票。
				</p>
				<p>
					专票与普票区别
				</p>
				<p>
					(1)使用的主体不同
				</p>
				<p>
					增值税专用发票一般只能由增值税一般纳税人领购使用，小规模纳税人需要使用的，只能经税务机关批准后由当地的税务机关代开；普通发票则可以由从事经营活动并办理了税务登记的各种纳税人领购使用。
				</p>
				<p>
					(2)税款是否允许抵扣
				</p>
				<p>
					增值税专用发票不仅是购销双方收付款的凭证，而且还可以用作购买方(增值税一般纳税人)扣除增值税的凭证，因此不仅具有商事凭证的作用，而且具备完税凭证的作用。而增值税普通发票除税法规定的经营项目外都不能抵扣进项税。
				</p>
				<br>
				<p>
					5、机动车销售统一发票
				</p>
				<p>
					机动车销售统一发票：凡从事机动车零售业务的单位和个人，从2006年8月1日起，在销售机动车(不包括销售旧机动车)收取款项时开具的发票。
				</p>
				<br>
				<p class="boldP">
					二、发票样式
				</p>
				<p>
					根据目前增值税的票种，分别展示了：增值税专用发票、机动车销售统一发票、货物运输业增值税专用发票（自2016年7月1日后停止使用）、增值税普通发票、增值税电子普通发票, 增值税普通发票(卷票)六种发票票样。
				</p>
				<br>
				<p>
					1、专用发票的基本联次为三联：发票联、抵扣联和记账联。
				</p>
				<p>
					发票联，作为购买方核算采购成本和增值税进项税额的记账凭证；抵扣联，作为购买方报送主管税务机关认证和留存备查的凭证；记账联，作为销售方核算销售收入和增值税销项税额的记账凭证。
				</p>
				<p>
					发票规格为240mm×140mm 。 
				</p>
				<br>
				<p>
					2、机动车销售统一发票
				</p>
				<p>
					机动车销售统一发票为电脑六联式发票。即第一联发票联(购货单位付款凭证)，第二联抵扣联(购货单位扣税凭证)，第三联报税联(车购税征收单位留存)，第四联注册登记联(车辆登记单位留存)，第五联记账联(销货单位记账凭证)，第六联存根联(销货单位留存)。第一联印色为棕色，第二联印色为绿色，第三联印色为紫色，第四联印色为蓝色，第五联印色为红色，第六联印色为黑色。发票代码、发票号码印色为黑色。
				</p>
				<p>
					发票规格为241mm×177mm 。
				</p>
				<br>
				<p>
					3、增值税普通发票
				</p>
				<p>
					增值税普通发票分为二联票和五联票，第一联：记账联，销货方记账凭证；第二联：发票联，购货方扣税凭证；第三联至第五联由发票使用单位自行安排使用。
				</p>
